    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        
        if(txtNome.getText().isEmpty()){
            AlertUtil.alert("Preencha o campo de nome!");
            return;
        }
        
        if(txtEmail.getText().isEmpty()){
            AlertUtil.alert("Preencha o campo de e-mail!");
            return;
        }
        
        Collaborator c = new Collaborator(txtNome.getText(), txtEmail.getText());
        c.addTool(new Slack(txtSlackID.getText(), txtSlackToken.getText()));
        
        new CollaboratorDAO().save(c);
        new FormSites().setVisible(true);
        this.setVisible(false);
        
    }//GEN-LAST:event_btSalvarActionPerformed

