    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (txtDescricao.getText().isEmpty()) {
            AlertUtil.alert("Preencha a descrição do projeto");
            return;
        }

        if (siteDAO.getSitesSelectedFromJList(listSites).isEmpty()) {
            AlertUtil.alert("Selecione um site!");
            return;
        }
        this.project.setDescription(txtDescricao.getText());
        this.project.setSites(siteDAO.getSitesSelectedFromJList(listSites));

        if (this.project.getId() == null) {
            System.out.println("save!");
            projectDAO.save(project);
        } else {
            System.out.println("update!");
            projectDAO.update(project);
        }

        AlertUtil.alert("Feito com sucesso!");
        new GerenciarProjetos().setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_btSalvarActionPerformed

