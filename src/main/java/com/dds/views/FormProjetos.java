    public void carregarListaDeSites() {
        DefaultListModel model = new DefaultListModel();
        List<Integer> indices = new ArrayList<>();
        int i = 0;
        for (Site site : this.siteDAO.findAll()) {
            model.addElement(site.getId() + " - " + site.getDescription());
            if (!(this.project.getId() == null)) {
                if (this.project.getSites().size() > 0) {
                    for (Site siteItem : this.project.getSites()) {
                        if (siteItem.getId() == site.getId()) {
                            indices.add(i);
                        }
                    }
                }
            }
            i++;
        }

        this.listSites.setModel(model);
        for (int id : indices) {
            System.out.println("seleciona");
            this.listSites.setSelectedIndex(id);
        }
    }

